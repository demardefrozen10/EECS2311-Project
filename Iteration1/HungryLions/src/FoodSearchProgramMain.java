package SearchFunction;


// Define a class for a food spot

class FoodSearchProgramMain {
    String name;
    String cuisineType;
    boolean isHalal;
    boolean isGlutenFree;
    boolean isVegan;
    boolean isVegetarian;

    public FoodSearchProgramMain(String name, String cuisineType, boolean isHalal, boolean isGlutenFree, boolean isVegan, boolean isVegetarian) {
        this.name = name;
        this.cuisineType = cuisineType;
        this.isHalal = isHalal;
        this.isGlutenFree = isGlutenFree;
        this.isVegan = isVegan;
        this.isVegetarian = isVegetarian;
    }
}
